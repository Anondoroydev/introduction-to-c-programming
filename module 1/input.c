#include <stdio.h> // input-output ফাংশনের জন্য

int main()
{
    // ================================
    // Variable Declaration (ভেরিয়েবল ঘোষণা)
    // ================================
    int a;   // পূর্ণসংখ্যা রাখবে
    float f; // দশমিক সংখ্যা রাখবে
    char c;  // একটি অক্ষর রাখবে

    // ================================
    // Input নেওয়া (user থেকে)
    // ================================
    // এক লাইনে int, float, char ইনপুট নেওয়া হচ্ছে
    scanf("%d %f %c", &a, &f, &c);

    /*
        ⚠️ গুরুত্বপূর্ণ:
        - &a → a এর address পাঠানো হয়
        - &f → f এর address
        - &c → c এর address
    */

    // ================================
    // Output দেখানো
    // ================================
    printf("%d %f %c", a, f, c);

    // ================================
    // Program শেষ
    // ================================
    return 0;
}