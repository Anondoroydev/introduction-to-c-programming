#include <stdio.h>

int main()
{
    // ================================
    // 1. Integer (পূর্ণসংখ্যা)
    // ================================
    int num1 = 10; // প্রথমে 10 assign করা হলো
    num1 = 20;     // পরে value পরিবর্তন করে 20 করা হলো

    printf("Integer value: %d\n", num1); // %d দিয়ে int print হয়

    // ================================
    // 2. Float (ছোট দশমিক সংখ্যা)
    // ================================
    float f = 4.56;

    // %.2f মানে দশমিকের পরে 2 ঘর দেখাবে
    printf("Float value: %.2f\n", f);

    // ================================
    // 3. Character (একটি অক্ষর)
    // ================================
    char c = '8';

    printf("Character value: %c\n", c); // %c দিয়ে char print হয়

    // ================================
    // 4. int overflow (ভুল উদাহরণ)
    // ================================
    // int a = 10000000000;
    // printf("%d\n", a);

    // ⚠️ সমস্যা: int এত বড় সংখ্যা রাখতে পারে না

    // ================================
    // 5. long long int (বড় সংখ্যা)
    // ================================
    long long int bigNum = 10000000000;

    printf("Long long value: %lld\n", bigNum); // %lld ব্যবহার করতে হয়

    // ================================
    // 6. float vs double
    // ================================
    float fa = 2134.5678923;
    printf("Float (less precise): %f\n", fa);

    double da = 2134.567892;
    printf("Double (more precise): %lf\n", da);

    // ================================
    // Program End
    // ================================
    return 0;
}