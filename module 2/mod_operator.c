#include <stdio.h>

int main()
{
    // ================================
    // Variable Declaration
    // ================================
    int a = 18; // প্রথম সংখ্যা
    int b = 5;  // দ্বিতীয় সংখ্যা

    // ================================
    // Modulus Operation (%)
    // ================================
    /*
        % (modulus) অপারেটর দিয়ে ভাগশেষ (remainder) বের করা হয়

        Example:
        18 ÷ 5 = 3 (quotient)
        remainder = 3

        তাই 18 % 5 = 3
    */
    int rem = a % b;

    // ================================
    // Output
    // ================================
    printf("rem = %d", rem);

    // ================================
    // Program End
    // ================================
    return 0;
}